// Keyboard navigation support
document.addEventListener('keydown', function(e) {
    // Escape key closes modals and dropdowns
    if (e.key === 'Escape') {
        const activeModals = document.querySelectorAll('.modal.active');
        const activeDropdowns = document.querySelectorAll('.user-dropdown.active');
        
        activeModals.forEach(modal => {
            modal.classList.remove('active');
        });
        
        activeDropdowns.forEach(dropdown => {
            dropdown.classList.remove('active');
        });
    }
    
    // Enter key activates focused elements
    if (e.key === 'Enter') {
        const focusedElement = document.activeElement;
        if (focusedElement && focusedElement.classList.contains('btn')) {
            focusedElement.click();
        }
    }
});

// Accessibility improvements
function improveAccessibility() {
    // Add ARIA labels to interactive elements
    document.querySelectorAll('.btn').forEach((btn, index) => {
        if (!btn.getAttribute('aria-label')) {
            btn.setAttribute('aria-label', btn.textContent.trim() || `Button ${index + 1}`);
        }
    });
    
    // Add focus indicators
    document.querySelectorAll('a, button, input, select, textarea').forEach(element => {
        element.addEventListener('focus', function() {
            this.style.outline = '2px solid #8B0000';
            this.style.outlineOffset = '2px';
        });
        
        element.addEventListener('blur', function() {
            this.style.outline = '';
            this.style.outlineOffset = '';
        });
    });
}

// Initialize accessibility improvements
document.addEventListener('DOMContentLoaded', improveAccessibility);

// Export functions for use in other scripts
